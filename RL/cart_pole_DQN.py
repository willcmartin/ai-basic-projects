# replay memory
# stores transitions, randomly reuse transitions to decorrelate batch

Transition = namedtuple('Transition', ('state', 'action', 'next_state', 'reward'))

class ReplayMemory:
    def __init__(self, capacity):
        self.memory = deque([], maxlen=capacity)

    def push(self, *args):
        self.memory.append(Transition(*args))

    def sample(self, batch_size):
        return random.sample(self.memory, batch_size)

    def __len__(self):
        return len(self.memory)

# DQN (Deep Q Learning)
# maximize cumulative reward (e.g. return)
# discount (0-1) ensures sum converges, weighted so that rewards from near future are more signification
# policy (state) = argmax Q(state, action)
# we want to find Q function: find temporal difference error
# Huber loss (mean squared error when error small and mean absolute error when error is large) robust to outliers
#   calculated from transistion batch from replay memory

# Q-network
# input: state (s) (diff between two image frames)
# ouputs: Q(s, left) Q(s, right)
# predict expected return given action left or right with the current state (s)

class DQN(nn.Module):
    def __init__(self, h, w, outputs):
        super(DQN, self).__init__()
        self.conv1 = nn.Conv2d(3, 16, kernel_size=5, stride=2)
        self.bn1 = nn.BatchNorm2d(16)
        self.conv2 = nn.Conv2d(16, 32, kernel_size=5, stride=2)
        self.bn2 = nn.BatchNorm2d(32)
        self.conv3 = nn.Conv2d(32, 32, kernel_size=5, stride=2)
        self.bn3 = nn.BatchNorm2d(32)

        # compute linear layer input size based on input image size
        def conv2d_size_out(size, kernal_size=5, stride=2):
            return (size - (kernal_size - 1) - 1) // stride + 1

        convw = conv2d_size_out(conv2d_size_out(conv2d_size_out(w)))
        convh = conv2d_size_out(conv2d_size_out(conv2d_size_out(h)))
        linear_input_size = convw * convh * 32

        self.head = nn.Linear(linear_input_size, outputs)

    def forward(self, x):
        x = x.to(device)
        x = F.relu(self.bn1(self.conv1(x)))
        x = F.relu(self.bn2(self.conv2(x)))
        x = F.relu(self.bn3(self.conv3(x)))

        return self.head(x.view(x.size(0), -1))

# input extraction: process images from environment
